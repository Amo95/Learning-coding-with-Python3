def addition(a, b):
  """We are performing a simple arithmetic  addition"""
  add = int(a + b)
  return add

def sub(a, b):
  """We are performing a simple arithmetic  subtraction"""
  minus = int(a - b)
  return minus

def multiple(a, b):
  """We are performing a simple arithmetic  multiplication"""
  times = int(a * b)
  return times

def divide(a, b):
  """We are performing a simple arithmetic  division"""
  div = float(a / b)
  return div
